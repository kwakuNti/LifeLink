                <h2>Can You Live with One Kidney?</h2>
                <p>
                    Most people living with one kidney—whether born with a single kidney or having donated a kidney—are able to live long, healthy lives. 
                    Living donors report feeling fulfilled after helping others in need.
                </p>
                <blockquote>
                    "Since donating my kidney, a number of people have approached me and told me what an amazing person I am. I just feel like a normal person who helped someone less fortunate." – Carrie
                </blockquote>
            </section>

            <section id="long-term-risks">
                <h2>Are There Any Long-Term Risks?</h2>
                <p>
                    Although risks are generally low, uncommon complications such as fatigue or persistent pain have been reported in rare cases.
                    Annual follow-ups ensure any issues like increased blood pressure or protein in urine are detected and managed.
                </p>
            </section>

            <section id="side-effects">
                <h2>What Are the Side Effects?</h2>
                <p>
                    A small percentage of donors experience increased blood pressure or slight changes in kidney function. Pregnant women may have an increased risk of gestational hypertension or pre-eclampsia.
                </p>
            </section>

            <section id="quality-of-life">
                <h2>Can Donating a Kidney Shorten Your Life?</h2>
                <p>
                    Studies show that kidney donors have equivalent or better survival rates compared to the general population. Risks of kidney disease post-donation are significantly lower than in the unscreened population.
                </p>
            </section>

